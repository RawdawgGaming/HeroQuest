// ── Registry (keyed by id) ────────────────────────────────────────────

export const ENEMY_ARCHETYPES: Record<string, EnemyArchetype> = {};
for (const arch of archetypeList) {
  ENEMY_ARCHETYPES[arch.id] = arch;
}

/** Look up an archetype by id. Throws if not found. */
export function getEnemyArchetype(id: string): EnemyArchetype {
  const arch = ENEMY_ARCHETYPES[id];
  if (!arch) {
    throw new Error(`Unknown enemy archetype: "${id}"`);
  }
  return arch;
}

